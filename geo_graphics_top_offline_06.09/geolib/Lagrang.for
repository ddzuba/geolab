	SUBROUTINE LAGRANG(N,XX,FF,X,F)
      IMPLICIT REAL*4(A-H,O,P,R-Z)
      DIMENSION X(N),F(N)
      OM=1.D0
      DO 1 I=1,N
	RAB=XX-X(I)
	IF(ABS(RAB).LT.0.00001) THEN
	FF=F(I)
	GOTO 777
	END IF
1     OM=OM*RAB
      FF=0.D0
      DO 2 I=1,N
      OM1=1.D0
      DO 3 J=1,N
      IF(I.EQ.J) GOTO3
      OM1=OM1*(X(I)-X(J))
3     CONTINUE
      FF=FF+OM/OM1*F(I)/(XX-X(I))  
2     CONTINUE
777   RETURN
      END 

	SUBROUTINE WATER(W_R,FLAG_20,FLAG_30)
	!USE PARAM_
	REAL*4 X(4),F_20(4),F_30(4)
	X(1)=0
	X(2)=2.5
	X(3)=5
	X(4)=7.5
	F_20(1)=0.297
	F_20(2)=0.310
	F_20(3)=0.325
	F_20(4)=0.343
	F_30(1)=0.310
	F_30(2)=0.326
	F_30(3)=0.343
	F_30(4)=0.362
	CALL LAGRANG(4,W_R,FLAG_20,X,F_20)
	CALL LAGRANG(4,W_R,FLAG_30,X,F_30)
	RETURN
	END SUBROUTINE WATER